// ===== CHAT MODELS =====

export type ConversationRole = 'Buyer' | 'Seller';
export type ConversationStatus = 'Active' | 'Closed' | 'Archived';
export type TemplateType = 'AutoReply' | 'Manual' | 'FAQ';
export type IntentType = 'Price' | 'Availability' | 'Details' | 'Negotiation' | 'Other';

export interface ConversationDto {
  id: string;
  adId: string;
  adTitle: string;
  buyerId: string;
  buyerName: string;
  sellerId: string;
  sellerName: string;
  status: ConversationStatus;
  lastMessage?: string;
  lastMessageAt?: Date;
  unreadCount: number;
  createdAt: Date;
  updatedAt: Date;
}

export interface DirectConversationDto {
  id: string;
  user1Id: string;
  user1Name: string;
  user2Id: string;
  user2Name: string;
  status: ConversationStatus;
  lastMessage?: string;
  lastMessageAt?: Date;
  unreadCount: number;
  createdAt: Date;
  updatedAt: Date;
}

export interface ChatMessageDto {
  id: string;
  conversationId?: string;
  directConversationId?: string;
  senderId: string;
  senderName: string;
  content: string;
  messageType: 'Text' | 'Image' | 'File' | 'System';
  isRead: boolean;
  createdAt: Date;
  editedAt?: Date;
  replyToId?: string;
}

export interface CreateConversationDto {
  adId: string;
  initialMessage: string;
}

export interface CreateDirectConversationDto {
  otherUserId: string;
  initialMessage: string;
}

export interface GetOrCreateDirectConversationDto {
  otherUserId: string;
}

export interface SendMessageDto {
  content: string;
  messageType?: 'Text' | 'Image' | 'File';
  replyToId?: string;
}

export interface SendDirectMessageDto {
  content: string;
  messageType?: 'Text' | 'Image' | 'File';
  replyToId?: string;
  directConversationId: string;
}

